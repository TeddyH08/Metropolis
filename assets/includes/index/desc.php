        <h3>Histoire de Metropolis</h3>
        <p>Le cinéma METROPOLIS naît d’une réflexion menée en 1997. Il paraît alors en effet certain que les 5 salles du cinéma «LES CLUBS» et la salle du «FORUM» ne correspondraient rapidement plus aux attentes du public. La restructuration complète des deux établissements est rapidement abandonnée. Un projet de construction d’un multiplexe de 10 salles est alors conduit.

